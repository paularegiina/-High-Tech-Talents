'''Faça um Programa que peça as 4 notas bimestrais e mostre a média.'''
nota_1 = float(input("Informe a nota 01: "))
nota_2 = float(input("Informe a nota 02: "))
nota_3 = float(input("Informe a nota 03: "))
nota_4 = float(input("Informe a nota 04: "))
media = (nota_1 + nota_2 + nota_3 + nota_4)/4
print(f'Media: {media}')